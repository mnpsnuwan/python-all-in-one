# Type Casting = Convert the data type of value to another data type

x = 1  # int
y = 2.0  # float
z = "3"  # str

print(x)
print(y)
print(z)

print(z*3)

print(str(x))
print(str(y))
print(str(z))

y = int(y)
print(y)

z = int(z)
print(z*3)

x = float(x)
y = float(y)
z = float(z)

print(x)
print(y)
print(z)

print("x is " + str(x))
print("y is " + str(y))

